"""
L01 Error Codes (E1000-E1999)
"""

from enum import Enum


class L01ErrorCode(str, Enum):
    """Error codes for L01 Data Layer."""

    # Database errors (E1000-E1099)
    DATABASE_CONNECTION_FAILED = "E1000"
    DATABASE_QUERY_FAILED = "E1001"
    DATABASE_TRANSACTION_FAILED = "E1002"
    DATABASE_CONSTRAINT_VIOLATION = "E1003"
    DATABASE_DEADLOCK = "E1004"

    # Redis errors (E1100-E1199)
    REDIS_CONNECTION_FAILED = "E1100"
    REDIS_PUBLISH_FAILED = "E1101"
    REDIS_SUBSCRIBE_FAILED = "E1102"

    # Validation errors (E1200-E1299)
    INVALID_INPUT = "E1200"
    SCHEMA_VALIDATION_FAILED = "E1201"
    MISSING_REQUIRED_FIELD = "E1202"
    INVALID_UUID = "E1203"
    INVALID_JSON = "E1204"

    # Not found errors (E1300-E1399)
    AGENT_NOT_FOUND = "E1300"
    TOOL_NOT_FOUND = "E1301"
    GOAL_NOT_FOUND = "E1302"
    PLAN_NOT_FOUND = "E1303"
    TASK_NOT_FOUND = "E1304"
    EVALUATION_NOT_FOUND = "E1305"
    CONFIG_NOT_FOUND = "E1306"
    EVENT_NOT_FOUND = "E1307"
    DOCUMENT_NOT_FOUND = "E1308"
    SESSION_NOT_FOUND = "E1309"

    # Conflict errors (E1400-E1499)
    AGENT_ALREADY_EXISTS = "E1400"
    TOOL_ALREADY_EXISTS = "E1401"
    CONFIG_ALREADY_EXISTS = "E1402"
    DUPLICATE_KEY = "E1403"

    # Service unavailable (E1500-E1599)
    SERVICE_UNAVAILABLE = "E1500"
    DATABASE_UNAVAILABLE = "E1501"
    REDIS_UNAVAILABLE = "E1502"
